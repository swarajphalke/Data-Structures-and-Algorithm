
public class SwitchStatement {
    public static void main(String[] args) {
        int number = 4;
        switch (number) {
            case 1:
                System.out.println("Samosa");
                break;
            case 2:
                System.out.println("Burger");
                break;
            case 3:
                
                System.out.println("Mango Shake");
                break;
            default:
                System.out.println("We have no food");
        }
    }
}
