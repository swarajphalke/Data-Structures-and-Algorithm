
public class AssignmentOp {
    public static void main(String[] args) {
        // = equal to
        // +=
        // -=
        // *=
        // /=
        int a = 5;
        // a = a + 10;
        a+=10;
        System.out.println(a);

        int b = 5;
        // b = b - 2;
        b-=2;
        System.out.println(b);
    }
}
