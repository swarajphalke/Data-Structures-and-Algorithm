import java.util.*;

public class TypeConversion {
    public static void main(String[] args) {
        // Type Conversion
        // widening
        // implicit

        Scanner sc = new Scanner(System.in);
        int a = 25;
        long b = a;
        System.out.println(b);

        float num = sc.nextInt();
        System.out.println(num);
        // byte -> short -> int -> float -> long -> double ->
    }
}
